#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	a = (char *) s;
	i = 0;
	while (a[i] != '\0' && a[i] != c)
	{
		i++;
	}
	if (a[i] == '\0')
	{
		if (c == '\0')
			return (&a[i]);
		else
			return (NULL);
	}
	return (&a[i]);
}
